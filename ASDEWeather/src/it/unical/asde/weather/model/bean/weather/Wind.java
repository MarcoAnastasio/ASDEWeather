package it.unical.asde.weather.model.bean.weather;

public class Wind {

	private Float speed;
	private Float deg;
	
	public Wind(){
		
	}
	
	public Float getSpeed() {
		return speed;
	}
	public void setSpeed(Float wind) {
		this.speed = wind;
	}
	public Float getDeg() {
		return deg;
	}
	public void setDeg(Float deg) {
		this.deg = deg;
	}
	
	
}

package it.unical.asde.weather.model.bean.weather;

public class Wind {

	private Float wind;
	private Float deg;
	
	public Wind(){
		
	}
	
	public Float getWind() {
		return wind;
	}
	public void setWind(Float wind) {
		this.wind = wind;
	}
	public Float getDeg() {
		return deg;
	}
	public void setDeg(Float deg) {
		this.deg = deg;
	}
	
	
}
